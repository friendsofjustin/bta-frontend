#!/bin/bash
Red='\033[0;31m'
Green='\033[0;32m'
Color_Off='\033[0m'

./deploy.sh local
res=$?
{ set +x; } 2>/dev/null
if [ $res -ne 0 ]; then
    echo -e "${Red}"
    echo "-----------------------------------------------------------------------"
    echo "-----------------------------------------------------------------------"
    echo "Failed to run BTA frontend application"
    echo "-----------------------------------------------------------------------"
    echo "-----------------------------------------------------------------------"
    echo -e "${Color_Off}"
    exit 1
  fi
echo -e "${Green}"
echo "-----------------------------------------------------------------------"
echo "-----------------------------------------------------------------------"
echo "Successfully created and run BTA frontend application"
echo "-----------------------------------------------------------------------"
echo "-----------------------------------------------------------------------"
echo -e "${Color_Off}"